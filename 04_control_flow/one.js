
// In JavaScript, `if` and `else` are control flow statements used for conditional branching, allowing you to execute different code blocks based on certain conditions.

// The basic syntax for an `if` statement is as follows:

// ```javascript
// if (condition) {
//   // Code to execute if the condition is true
// } else {
//   // Code to execute if the condition is false
// }
// ```

// Here's a breakdown of the components:

// - `if`: This is the keyword that starts the `if` statement.
// - `condition`: This is the expression that is evaluated to determine whether it's true or false.
// - `code to execute if the condition is true`: This is the code block that executes if the condition is true.
// - `else`: This is an optional keyword that is followed by a code block to execute if the condition is false.

// Here's an example of how you might use an `if` statement in JavaScript:

// ```javascript
// const age = 25;

// if (age >= 18) {
//   console.log('You are an adult.');
// } else {
//   console.log('You are a minor.');
// }
// ```

// In this example, if the `age` variable is greater than or equal to 18, the message "You are an adult." will be logged to the console. If the `age` is less than 18, the message "You are a minor." will be logged instead.


